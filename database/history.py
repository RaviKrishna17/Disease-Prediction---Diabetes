import json
import datetime
from sqlalchemy.orm import Session
from sqlalchemy import or_, desc, asc
from database.models import PredictionHistory, LoginHistory

def record_login(db: Session, user_id: int, login_method: str, ip_address: str, 
                 browser: str, operating_system: str, device_type: str) -> LoginHistory:
    """Save a login event log inside login_histories."""
    history = LoginHistory(
        user_id=user_id,
        login_time=datetime.datetime.utcnow(),
        ip_address=ip_address,
        browser=browser,
        operating_system=operating_system,
        device_type=device_type,
        login_method=login_method
    )
    db.add(history)
    db.commit()
    db.refresh(history)
    return history

def record_logout(db: Session, user_id: int):
    """Update logout_time on the most recent login log for this user."""
    history = db.query(LoginHistory).filter(
        LoginHistory.user_id == user_id,
        LoginHistory.logout_time.is_(None)
    ).order_by(desc(LoginHistory.login_time)).first()
    
    if history:
        history.logout_time = datetime.datetime.utcnow()
        db.commit()
        db.refresh(history)
        return history
    return None

def save_prediction(db: Session, user_id: int, prediction_id: str, date_str: str,
                    age: int, gender: str, height: float, weight: float, bmi: float,
                    blood_sugar: float, hba1c: float, blood_pressure: str,
                    health_history: dict, lifestyle_factors: dict,
                    uploaded_report_name: str, prediction_percentage: float,
                    risk_category: str, prediction_result: str,
                    recommendation: list) -> PredictionHistory:
    """Store finished prediction payload in prediction_histories."""
    pred = PredictionHistory(
        id=prediction_id,
        user_id=user_id,
        prediction_date=date_str,
        age=age,
        gender=gender,
        height=height,
        weight=weight,
        bmi=bmi,
        blood_sugar=blood_sugar,
        hba1c=hba1c,
        blood_pressure=blood_pressure,
        health_history=json.dumps(health_history),
        lifestyle_factors=json.dumps(lifestyle_factors),
        uploaded_report_name=uploaded_report_name,
        prediction_percentage=prediction_percentage,
        risk_category=risk_category,
        prediction_result=prediction_result,
        recommendation=json.dumps(recommendation)
    )
    db.add(pred)
    db.commit()
    db.refresh(pred)
    return pred

def get_prediction_count(db: Session, user_id: int) -> int:
    """Get the total number of predictions generated by a user."""
    return db.query(PredictionHistory).filter(PredictionHistory.user_id == user_id).count()

def get_user_predictions(db: Session, user_id: int, search_term: str = None, 
                         sort_by: str = None, page: int = 1, limit: int = 10):
    """
    Retrieve paginated user predictions with search and sorting criteria.
    Returns:
        (predictions_list, total_count)
    """
    query = db.query(PredictionHistory).filter(PredictionHistory.user_id == user_id)
    
    # 1. Apply Search
    if search_term:
        term = f"%{search_term}%"
        query = query.filter(
            or_(
                PredictionHistory.id.ilike(term),
                PredictionHistory.risk_category.ilike(term),
                PredictionHistory.prediction_result.ilike(term),
                PredictionHistory.prediction_date.ilike(term)
            )
        )
        
    total_count = query.count()

    # 2. Apply Sorting
    if sort_by == 'risk_percentage_desc':
        query = query.order_by(desc(PredictionHistory.prediction_percentage))
    elif sort_by == 'risk_percentage_asc':
        query = query.order_by(asc(PredictionHistory.prediction_percentage))
    elif sort_by == 'date_asc':
        # Default string sort is not fully temporal, but helps or we sort by id/date desc
        query = query.order_by(asc(PredictionHistory.id))
    else:
        # Default: Newest prediction first
        query = query.order_by(desc(PredictionHistory.id))

    # 3. Apply Pagination
    offset = (page - 1) * limit
    results = query.offset(offset).limit(limit).all()
    
    # Deserialize list/dict properties for easy JSON serialization
    serialized_results = []
    for r in results:
        serialized_results.append({
            "id": r.id,
            "date": r.prediction_date,
            "age": r.age,
            "gender": r.gender,
            "height": r.height,
            "weight": r.weight,
            "bmi": r.bmi,
            "glucose": r.blood_sugar,
            "hba1c": r.hba1c,
            # We also provide systolic/diastolic splitting to match original JSON UI keys
            "systolicBP": float(r.blood_pressure.split('/')[0]) if '/' in r.blood_pressure else 120.0,
            "diastolicBP": float(r.blood_pressure.split('/')[1]) if '/' in r.blood_pressure else 80.0,
            "health_history": json.loads(r.health_history),
            "lifestyle_factors": json.loads(r.lifestyle_factors),
            "uploaded_report_name": r.uploaded_report_name,
            "riskPercentage": r.prediction_percentage,
            "riskLevel": r.risk_category,
            "prediction": r.prediction_result,
            "recommendations": json.loads(r.recommendation),
            # Keep original clinicalNotes placeholder to avoid breaking front/back styling
            "clinicalNotes": f"Patient exhibits {r.risk_category.lower()} risk indicators with a predicted result of {r.prediction_result.lower()}."
        })
        
    return serialized_results, total_count

def delete_user_prediction(db: Session, user_id: int, prediction_id: str) -> bool:
    """Delete a single prediction record for security verification."""
    record = db.query(PredictionHistory).filter(
        PredictionHistory.user_id == user_id,
        PredictionHistory.id == prediction_id
    ).first()
    
    if record:
        db.delete(record)
        db.commit()
        return True
    return False
